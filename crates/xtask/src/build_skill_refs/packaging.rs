pub fn packaging(repo_root: &Path) -> Result<Generated> {
    let mut body = String::new();
    body.push_str("# Toolr command-packaging reference: plugin manifest\n\n");
    body.push_str(DO_NOT_EDIT);
    body.push_str("\n\n");
    body.push_str(
        "This reference is generated from `toolr-core`'s own types. The \
        struct definitions below are extracted verbatim from \
        `crates/toolr-core/src/manifest/model.rs` and \
        `crates/toolr-core/src/third_party/model.rs`. If you ship a \
        plugin whose `toolr-manifest.json` matches the schema below, \
        toolr's loader will accept it. If you ship one that doesn't, \
        the load will fail with a clear error from \
        `parse_fragment`.\n\n",
    );

    body.push_str("## Discovery\n\n");
    body.push_str(
        "Toolr discovers your plugin by globbing every installed \
        wheel's package root for a file named `toolr-manifest.json`. \
        The exact globbing pattern is in \
        `crates/toolr-core/src/third_party/glob.rs`:\n\n",
    );
    body.push_str("```text\n");
    body.push_str(&glob_summary());
    body.push_str("```\n\n");
    body.push_str(
        "Either layout works on every supported platform. If your \
        wheel installs `<pkg>/toolr-manifest.json` next to the \
        package's `__init__.py`, the glob picks it up.\n\n",
    );

    for section in SECTIONS {
        let extract = extract_region(section.source.0, section.source.1)?;
        let _ = writeln!(body, "## {}\n", section.heading);
        body.push_str(section.narrative);
        body.push_str("\n\n");
        body.push_str("```rust\n");
        body.push_str(&extract);
        body.push_str("```\n\n");
    }

    // Trim trailing whitespace + ensure exactly one trailing newline.
    let mut trimmed = body.trim_end().to_string();
    trimmed.push('\n');

    Ok(Generated {
        path: repo_root
            .join("skills/toolr-command-packaging/references/packaging.md"),
        body: trimmed,
    })
}

fn extract_region(file: &str, tag: &str) -> Result<String> {
    let source = match file {
        "manifest/model.rs" => MANIFEST_MODEL,
        "third_party/model.rs" => FRAGMENT_MODEL,
        other => {
            return Err(anyhow!(
                "build_skill_refs::packaging: no embedded copy of `{other}` \
                 — add an `include_str!` entry above SECTIONS",
            ));
        }
    };
    let start_marker = format!("// region: {tag}");
    let end_marker = format!("// endregion: {tag}");

    let start = source
        .find(&start_marker)
        .ok_or_else(|| anyhow!("missing `{start_marker}` in {file}"))?;
    // Step past the start marker line.
    let after_start = source[start..]
        .find('\n')
        .map(|n| start + n + 1)
        .ok_or_else(|| anyhow!("malformed start marker in {file}"))?;
    let end = source[after_start..]
        .find(&end_marker)
        .map(|n| after_start + n)
        .ok_or_else(|| anyhow!("missing `{end_marker}` in {file} (after {tag} start)"))?;

    // Drop the blank line we'd otherwise leave between the last item
    // and the endregion marker. Trim once, append a single newline.
    let mut extract = source[after_start..end].trim_end().to_string();
    extract.push('\n');
    Ok(extract)
}

fn glob_summary() -> String {
    // Pull the first few lines of the module doc so the reference text
    // matches what the source itself documents.
    let mut lines = Vec::new();
    for line in GLOB_MODULE.lines() {
        if let Some(stripped) = line.strip_prefix("//! ") {
            lines.push(stripped.to_string());
        } else if line == "//!" {
            lines.push(String::new());
        } else if !line.is_empty() {
            break;
        }
    }
    // Trim trailing blank lines.
    while matches!(lines.last(), Some(s) if s.is_empty()) {
        lines.pop();
    }
    let mut joined = lines.join("\n");
    joined.push('\n');
    joined
}

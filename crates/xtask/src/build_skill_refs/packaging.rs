//! Generator for the `toolr-command-packaging` skill's
//! `references/packaging.md`.
//!
//! Source of truth is the Rust serde types under `toolr-core`. Rather
//! than hand-rolling a JSON schema (and watching it rot), the generator
//! embeds the relevant Rust source verbatim via `include_str!` and
//! extracts named regions delimited by `// region: <Tag>` /
//! `// endregion: <Tag>` markers in the source files.
//!
//! Adding a new section: bracket the relevant type with markers in the
//! source file, then add an entry to [`SECTIONS`].

use std::fmt::Write;
use std::path::Path;

use anyhow::{Result, anyhow};

use super::Generated;

const DO_NOT_EDIT: &str =
    "<!-- generated by `cargo xtask build-skill-refs`; do not edit by hand -->";

/// Rust source files whose region markers feed the packaging reference.
const MANIFEST_MODEL: &str =
    include_str!("../../../toolr-core/src/manifest/model.rs");
const FRAGMENT_MODEL: &str =
    include_str!("../../../toolr-core/src/third_party/model.rs");
const GLOB_MODULE: &str =
    include_str!("../../../toolr-core/src/third_party/glob.rs");

/// A `## <heading>` section of the rendered reference. Each section
/// pairs hand-written narrative with a verbatim Rust extract.
struct Section {
    heading: &'static str,
    /// Markdown body — should be written so it reads naturally with a
    /// trailing fenced rust block appended.
    narrative: &'static str,
    /// `(file, tag)` for the region the generator should extract and
    /// append after the narrative.
    source: (&'static str, &'static str),
}

